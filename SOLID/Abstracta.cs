using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SOLID
{
    internal class Abstracta
    {
        /*
 
         * Una clase abstracta en C# es una clase que no se puede instanciar directamente, 
         * sino que sirve como un modelo o plantilla para otras clases. 
         * Las clases abstractas pueden contener métodos abstractos, así como métodos 
         * concretos y campos como cualquier otra clase.
        
        Aquí hay algunos puntos importantes sobre las clases abstractas:

         1- No se pueden instanciar directamente: No puedes crear una instancia de una clase abstracta. 
        Solo puedes crear instancias de sus clases derivadas (subclases).

      2-Puede contener métodos abstractos y concretos: Una clase abstracta puede tener 
        métodos abstractos, que son métodos que solo se declaran pero no se implementan 
        en la clase abstracta. Las subclases deben proporcionar una implementación 
        para estos métodos abstractos. Además, una clase abstracta puede tener métodos concretos, es decir, métodos que tienen una implementación.

    3- Puede tener campos, propiedades y eventos: Al igual que cualquier otra clase, 
        una clase abstracta puede tener campos, propiedades, eventos y constructores.

    4- Se utiliza para la herencia y la polimorfismo: Las clases abstractas son útiles cuando 
        quieres definir un comportamiento común para varias clases relacionadas, 
        pero no quieres que la clase abstracta se instancie directamente.
        Las clases derivadas (subclases) extienden la clase abstracta 
        y proporcionan implementaciones concretas para sus métodos abstractos.

*/
    }
}
